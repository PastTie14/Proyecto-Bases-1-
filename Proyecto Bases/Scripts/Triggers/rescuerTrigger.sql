CREATE OR REPLACE TRIGGER beforeUpdateRescuer
BEFORE INSERT OR UPDATE
ON rescuer
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :new.createdBY := USER;
        :new.createdAt := SYSTIMESTAMP;

    ELSE
        :new.modifiedBY := USER;
        :new.modifiedAt := SYSTIMESTAMP;
    END IF;
END beforeUpdateRescuer;