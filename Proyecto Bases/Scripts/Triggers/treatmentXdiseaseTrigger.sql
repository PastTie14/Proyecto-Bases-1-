CREATE OR REPLACE TRIGGER beforeUpdateTxDisease
BEFORE INSERT OR UPDATE
ON treatment_x_disease
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :new.createdBY := USER;
        :new.createdAt := SYSTIMESTAMP;

    ELSE
        :new.modifiedBY := USER;
        :new.modifiedAt := SYSTIMESTAMP;
    END IF;
END beforeUpdateTxDisease;