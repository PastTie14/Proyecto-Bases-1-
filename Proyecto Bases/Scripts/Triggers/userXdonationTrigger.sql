CREATE OR REPLACE TRIGGER beforeUpdateDonationXUser
BEFORE INSERT OR UPDATE
ON donation_x_user
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :new.createdBY := USER;
        :new.createdAt := SYSTIMESTAMP;

    ELSE
        :new.modifiedBY := USER;
        :new.modifiedAt := SYSTIMESTAMP;
    END IF;
END beforeUpdateDonationXUser;