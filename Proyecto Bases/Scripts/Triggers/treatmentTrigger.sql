CREATE OR REPLACE TRIGGER beforeUpdateTreatment
BEFORE INSERT OR UPDATE
ON treatment
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :new.createdBY := USER;
        :new.createdAt := SYSTIMESTAMP;

    ELSE
        :new.modifiedBY := USER;
        :new.modifiedAt := SYSTIMESTAMP;
    END IF;
END beforeUpdateTreatment;